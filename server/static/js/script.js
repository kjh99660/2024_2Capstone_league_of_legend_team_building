fetch(url="/api/analyze?region=" + region + "&nickname=" + encodeURIComponent(nickname))
    .then((response) => response.json())
    .then((data) => {
        dataStore = {
            "all": [],
            "top": [],
            "jng": [],
            "mid": [],
            "bot": [],
            "sup": []
        };

        let method = data["method"];
        let recommends = data["recommends"];
        let champion_list = recommends["champion_list"];
        let line_list = recommends["line_list"];
        
        for (let i = 0; i < champion_list.length; i++) {
            let champion = champion_list[i].toLowerCase();
            let line = line_list[i];
            if (!dataStore["all"].includes(champion))
                dataStore["all"].push(champion);

            switch (line) {
                case "TOP":
                    dataStore["top"].push(champion);
                    break;
                case "JUNGLE":
                    dataStore["jng"].push(champion);
                    break;
                case "MIDDLE":
                    dataStore["mid"].push(champion);
                    break;
                case "BOTTOM":
                    dataStore["bot"].push(champion);
                    break;
                case "UTILITY":
                    dataStore["sup"].push(champion);
                    break;
            }
        }

        features = document.getElementById("features");
        features.innerHTML = '';

        if (method == "newbie") {
            features.appendChild(createFeature("초보자", "/static/img/newbie.png"));
        } else if (method == "class") {
            features.appendChild(createFeature("역할군", "/static/img/class.png"));
        } else if (method == "analyze") {
            let chrtics = data["characteristics"];
            
            let text1 = "공격성";
            let src = ["/static/img/defensive.png", "/static/img/neutral.png", "/static/img/offensive.png"];
            let text2 = ""
            switch (chrtics["aggression"]) {
                case -1:
                    text2 = "수비적";
                    break;
                case 0:
                    text2 = "중립적";
                    break;
                case 1:
                    text2 = "공격적";
                    break;
            }

            features.appendChild(createFeature2(text1, text2, src, chrtics["aggression"]));

            text1 = "로밍성";
            src = ["/static/img/lane.png", "/static/img/neutral.png", "/static/img/roam.png"]

            switch (chrtics["roamness"]) {
                case -1:
                    text2 = "라인전 위주";
                    break;
                case 0:
                    text2 = "중립적";
                    break;
                case 1:
                    text2 = "로밍 위주";
                    break;
            }

            features.appendChild(createFeature2(text1, text2, src, chrtics["roamness"]));

            text1 = "백도어";
            src = ["/static/img/fight.png", "/static/img/neutral.png", "/static/img/backdoor.png"]

            switch (chrtics["backdoor"]) {
                case -1:
                    text2 = "한타 위주";
                    break;
                case 0:
                    text2 = "중립적";
                    break;
                case 1:
                    text2 = "백도어 위주";
                    break;
            }

            features.appendChild(createFeature2(text1, text2, src, chrtics["backdoor"]));
        }

        select("all");
    }
);

function createFeature(text, src) {
    const div = document.createElement("div");
    div.className = "analysis_item";

    const span = document.createElement("span");
    span.textContent = text;

    const img = document.createElement("img");
    img.src = src;

    div.appendChild(span);
    div.appendChild(img);
    
    return div;
}

function createFeature2(text1, text2, src, selected) {
    const div = document.createElement("div");
    div.className = "analysis_item";

    const span1 = document.createElement("span");
    span1.textContent = text1;

    const innerdiv = document.createElement("div");

    console.log(src.length)
    for (let i = 0; i < src.length; i++) {
        const img = document.createElement("img");
        img.src = src[i];
        if (i == selected + 1) {
            img.className = "selected_feature";
        } else {
            img.className = "nonselected_feature";
        }
    innerdiv.appendChild(img)
    }

    const span2 = document.createElement("span");
    span2.textContent = text2;

    div.appendChild(span1);
    div.appendChild(innerdiv);
    div.appendChild(span2);
    
    return div;
}

function select(lane) {
    document.getElementById("select_all").src = "https://s-lol-web.op.gg/images/icon/icon-position-all-dark.svg";
    document.getElementById("select_top").src = "https://s-lol-web.op.gg/images/icon/icon-position-top-dark.svg";
    document.getElementById("select_jng").src = "https://s-lol-web.op.gg/images/icon/icon-position-jng-dark.svg";
    document.getElementById("select_mid").src = "https://s-lol-web.op.gg/images/icon/icon-position-mid-dark.svg";
    document.getElementById("select_bot").src = "https://s-lol-web.op.gg/images/icon/icon-position-bot-dark.svg";
    document.getElementById("select_sup").src = "https://s-lol-web.op.gg/images/icon/icon-position-sup-dark.svg";

    document.getElementById("select_" + lane).src = "https://s-lol-web.op.gg/images/icon/icon-position-" + lane + "-wh.svg";

    recommend = document.getElementById("recommend");
    recommend.innerHTML = '';

    dataStore[lane].forEach(element => {
        const li = document.createElement("li");
        const div = document.createElement("div");
        div.className = "champion_item";

        const img = document.createElement("img");
        img.src = champions[element]["src"];

        const span = document.createElement('span');
        span.textContent = champions[element]["text"];

        div.appendChild(img);
        div.appendChild(span);
        li.appendChild(div);

        recommend.appendChild(li);
    });
}